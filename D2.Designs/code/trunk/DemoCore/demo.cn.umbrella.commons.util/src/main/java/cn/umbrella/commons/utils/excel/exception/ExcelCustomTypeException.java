package cn.umbrella.commons.utils.excel.exception;

public class ExcelCustomTypeException  extends Exception{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	
	public ExcelCustomTypeException() {
		super();
	}

	public ExcelCustomTypeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ExcelCustomTypeException(String message) {
		super(message);
	}

	public ExcelCustomTypeException(Throwable cause) {
		super(cause);
	}

}
